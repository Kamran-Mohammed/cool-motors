function processCarModels(input) {
  const lines = input.map((line) => line.trim()).filter((line) => line !== "");

  // Remove duplicates (case-insensitive) and sort alphabetically
  const unique = [...new Set(lines)];
  unique.sort((a, b) => a.localeCompare(b));

  // Print in array form
  // console.log(JSON.stringify(unique, null, 2));

  // Modify the original array in place
  input.length = 0; // clear original array
  input.push(...unique); // push new items into it
  return unique;
}

